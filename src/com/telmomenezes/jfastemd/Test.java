    static Signature getSignature(double[] map)
    {
        int n = 0;
        for (int x = 0; x < bin_number; x++) {
            for (int y = 0; y < bin_number; y++) {
                if (getValue(map, x, y) > 0) {
                    n++;
                }
            }
        }
        

        Feature2D[] features = new Feature2D[n];
        double[] weights = new double[n];
        
        int i = 0;
        for (int x = 0; x < bin_number; x++) {
            for (int y = 0; y < bin_number; y++) {
                double val = getValue(map, x, y);
                if (val > 0) {
                    Feature2D f = new Feature2D(x, y);
                    features[i] = f;
                    weights[i] = val;
                    i++;
                }
            }
        }

        Signature signature = new Signature();
        signature.setNumberOfFeatures(n);
        signature.setFeatures(features);
        signature.setWeights(weights);

        return signature;
    }

    static double emdDist(double[] map1, double[] map2)
    {
        Signature sig1 = getSignature(map1);
        Signature sig2 = getSignature(map2);

        JFastEMD fastemd = new JFastEMD();
        double dist = fastemd.distance(sig1, sig2, -1);

        return dist;
    }
    
